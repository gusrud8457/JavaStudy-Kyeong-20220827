package j04_입력;

import java.util.Scanner;

public class Input02 {

	public static void main(String[] args) {
		/*
		 * 문자열1 입력 : nextLine()
		 * 문자열2 입력 : next()
		 * 문자 입력 : next().charAt(0)
		 * 정수 입력 : nextInt()
		 * 실수 입력 : nextDouble()
		 * 
		 */
		
		Scanner scanner = new Scanner(System.in);
		
		scanner.nextLine();
		scanner.next();
		
	}

}
